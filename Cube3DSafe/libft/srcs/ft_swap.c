#include "../includes/libft.h"

void	ft_swap(void **s1, void **s2)
{
	void	*stock;

	if (!s1 || !s2 || !*s1 || !*s2)
		return ;
	stock = *s2;
	*s2 = *s1;
	*s1 = stock;
}
